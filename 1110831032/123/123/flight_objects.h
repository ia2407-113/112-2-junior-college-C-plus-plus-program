#ifndef FLIGHT_OBJECTS_H
#define FLIGHT_OBJECTS_H

#include <iostream>
#include <string>
using namespace std;

// 飛行物體類別, 是一種抽象類別 
class flight_object {
public:
    string name;  // 飛行物名稱 
    int id;       // 飛行物編號 
    int pilot;    // 駕駛員人數 
    float kerosene; // 煤油量 (煤油是飛行物使用的燃料)           
    string manufacturer;  // 製造者

    // 建立飛行物的資料
    // create_flight_object 為純虛擬函式
    virtual void create_flight_object() = 0;

    // 顯示飛行物的資料
    // display 為純虛擬函式
    virtual void display() = 0;

    // 後燃器
    virtual void afterburner() = 0;
};

// 大型客機類別
class airliner : public flight_object {
private:
    int passenger;  // 乘客人數   
    int service_person;  // 服務人員的數目 
    int afterburner_time; // 後燃器秒數

public:
    // 建立大型客機的部分資料    
    void create_airliner();

    // 建立大型客機的部分資料
    // 純虛擬函式 create_flight_object 的實作內容 
    virtual void create_flight_object();

    // 顯示大型客機的資料
    // 純虛擬函式 display 的實作內容 
    virtual void display();

    // 後燃器
    virtual void afterburner();
};

// 戰鬥機類別 
class battleplane : public flight_object {
private:
    string weapon;
    int afterburner_time; // 後燃器秒數

public:
    // 建立戰鬥機的部分資料 
    void create_battleplane();

    // 建立戰鬥機的部分資料
    // 純虛擬函式 create_flight_object 的實作內容 
    virtual void create_flight_object();

    // 顯示戰鬥機的資料
    // 純虛擬函式 display 的實作內容     
    virtual void display();

    // 後燃器
    virtual void afterburner();
};

#endif // FLIGHT_OBJECTS_H
