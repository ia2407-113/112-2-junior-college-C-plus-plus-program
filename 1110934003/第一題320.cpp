#include <iostream> // 包含標準輸入輸出的標頭文件
using namespace std; // 使用標準命名空間

int main() { // 主函數的開始
    int n; // 宣告變數n，用於存儲使用者輸入的正整數
    double sum = 0.0; // 宣告變數sum，用於存儲1+1/2+1/3+...+1/n的和
    // 提示使用者輸入小於50的正整數n
    cout << "請輸入小於50的正整數n: ";
    cin >> n; // 從標準輸入讀取使用者輸入的值並存入變數n中
    // 確保輸入的數字在有效範圍內
    if (n <= 0 || n >= 50) {
        cout << "請輸入有效範圍內的正整數！" << endl; // 如果n不在有效範圍內，輸出錯誤訊息
        return 1; // 結束程式並返回1（代表錯誤）
    }
    // 計算 1+1/2+1/3+...+1/n 的和
    for (int i = 1; i <= n; ++i) {
        sum += 1.0 / i; // 將1/i加到sum中
    }
    // 輸出結果
    cout << "1+1/2+1/3+...+1/" << n << " 的和為: " << sum << endl; // 輸出1+1/2+1/3+...+1/n的和
    return 0; // 程式執行完畢並返回0（代表成功）
}
